package model;

public class Todo {

  private String id;
  private String todo;

  public Todo() {
  }

  public String getId() {

    return id;
  }

  public void setId(String id) {

    this.id = id;
  }

  public String getTodo() {

    return todo;
  }

  public void setTodo(String todo) {

    this.todo = todo;
  }
}
